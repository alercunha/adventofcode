def day1(input):
    length = len(input)
    return sum(
        int(input[x])
        for x in range(length)
        if input[x] == input[(x + 1) % length]
    )

assert(day1('1122') == 3)
assert(day1('1111') == 4)
assert(day1('1234') == 0)
assert(day1('91212129') == 9)

